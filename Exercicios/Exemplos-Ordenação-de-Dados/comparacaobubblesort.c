#include <stdio.h>
#include <time.h>
#include <stdlib.h>
#define TAMANHO 10000
// Prototipo de Função
void geraNumero(int *vet, int op);
void bubbleSort(int *vet);
void imprimirVetor(int *vet);
// Variáveis Globais
int qtd_comparacoes, qtd_trocas;
int main(void) {
	// Variáveis
	int vet1[TAMANHO];
	float tempo_inicial, tempo_final;
	printf("Ordenado:\n");
	geraNumero(vet1,1);
	tempo_inicial = clock();
	bubbleSort(vet1);
	tempo_final = clock() - tempo_inicial;
	printf("\nQuantidade de comparacoes: %i\n",qtd_comparacoes);
    printf("Quantidade de trocas: %i\n",qtd_trocas);
    printf("Tempo de execucao do algoritmo: %.3f\n",tempo_final/1000);
	printf("\nInvertido:\n");
	geraNumero(vet1,2);
	tempo_inicial = clock();
	bubbleSort(vet1);
	tempo_final = clock() - tempo_inicial;
	printf("\nQuantidade de comparacoes: %i\n",qtd_comparacoes);
    printf("Quantidade de trocas: %i\n",qtd_trocas);
    printf("Tempo de execucao do algoritmo: %.3f\n",tempo_final/1000);
	printf("\nAleatorio:\n");
	geraNumero(vet1,3);
	tempo_inicial = clock();
	bubbleSort(vet1);
	tempo_final = clock() - tempo_inicial;
	printf("\nQuantidade de comparacoes: %i\n",qtd_comparacoes);
    printf("Quantidade de trocas: %i\n",qtd_trocas);
    printf("Tempo de execucao do algoritmo: %.3f\n",tempo_final/1000);
	return 0;
}
// Função geração de números
void geraNumero(int *vet, int op) {
    int i, j;
    switch (op) {
        case 1: // Ordenados
            for (i = 0; i < TAMANHO; i++) {
                vet[i] = i + 1;
            }
            break;
        case 2: //Invertidos
            for (i = 0; i < TAMANHO; i++) {
               vet[i] = TAMANHO-i;
            }
            break;
        case 3: // Aleatórios
            for (i = 0; i < TAMANHO; i++) {
                vet[i] = (int) (rand() % TAMANHO);
            }
            break;
    }
}
// Função ordenação bubble sort
void bubbleSort(int *vet){
	int n=1, troca =1, i, aux;
	qtd_trocas = 0; qtd_comparacoes = 0;
    while (n <= TAMANHO && troca == 1) {
        troca = 0;
        for (i = 0; i <= TAMANHO-2; i++) {
        	// Ponto do algoritmo para contar as comparações
        	qtd_comparacoes++;
            if (vet[i] > vet[i + 1]) {
            	// Ponto do algoritmo para contar as trocas
            	qtd_trocas++;
                troca = 1;
                aux = vet[i];
                vet[i] = vet[i + 1];
                vet[i + 1] = aux;
            }
        }
        n = n + 1;
    }
}
// Função impressão do vetor
void imprimirVetor(int *vet){
	int i;
	for(i=0;i<TAMANHO;i++){
		printf("%i, ",vet[i]);
		if ((i+1)%14 == 0){
			printf("\n");
		}
	}
}
